    <h4 class="fw-bold mt-4 text-green">I.- Definiciones.</h4>

    <h5 class="fw-bold mt-3">Agregador Bancario</h5>
    <p>
        Se refiere a la empresa especializada que ha sido contratada por el Responsable para procesar los pagos realizados
        con tarjeta bancaria por el Titular.
    </p>

    <h5 class="fw-bold mt-3">Aviso</h5>
    <p>
        El presente aviso de privacidad, ya sea expedido en documento físico, electrónico o en cualquier otro formato
        generado por el Responsable que es puesto a disposición del Titular, previo al Tratamiento de sus Datos Personales.
    </p>

    <h5 class="fw-bold mt-3">Datos Personales</h5>
    <p>
        Cualquier información concerniente a una persona física identificada o identificable.
    </p>

    <h5 class="fw-bold mt-3">Datos Personales Sensibles</h5>
    <p>
        Aquellos Datos Personales que afecten a la esfera más íntima de su Titular, o cuya utilización indebida pueda dar
        origen a discriminación o conlleve un riesgo grave para éste. En particular, se consideran sensibles aquellos que
        puedan revelar aspectos como origen racial o étnico, estado de salud presente y futuro, información genética,
        creencias religiosas, filosóficas y morales, afiliación sindical, opiniones políticas o preferencia sexual.
    </p>

    <h5 class="fw-bold mt-3">Derechos “ARCO”</h5>
    <p>
        Se refiere al conjunto de prerrogativas que tiene el Titular frente al Responsable, con relación a sus Datos
        Personales, mismas que consisten en síntesis en lo siguiente: el derecho de acceder a sus Datos Personales y a los
        detalles del tratamiento de los mismos; así como a rectificarlos en caso de ser inexactos; cancelarlos cuando
        considere que resulten ser excesivos o innecesarios para las finalidades que justificaron su obtención; y oponerse
        al tratamiento de los mismos para fines específicos.
    </p>

    <h5 class="fw-bold mt-3">Encargado</h5>
    <p>
        La persona física o moral que sola o conjuntamente con otras trate Datos Personales por cuenta del Responsable.
        Jüsto.- Se refiere a Poder Justo, S.A.P.I. de C.V.
    </p>

    <h5 class="fw-bold mt-3">Ley</h5>
    <p>
        Ley Federal de Protección de Datos Personales en Posesión de Particulares.
    </p>

